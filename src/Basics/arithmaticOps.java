package Basics;

public class arithmaticOps {
    public static void main(String[] args) {
        int num1=5;
        int num2=2;
//        Addition
        System.out.println(num1+num2);
//        Subtraction
        System.out.println(num1-num2);
//        multiple
        System.out.println(num1*num2);
//        divided
        System.out.println(num1/num2);
//        module
        System.out.println(num1%num2);
    }
}
